    SET_VELOCITY_CONFIG_V_FF = 0x31
    SET_VELOCITY_CONFIG_PSIDOT = 0x32
    SET_VELOCITY_CONFIG_PSIDOT_FF = 0x33

    SET_POSITION_CONFIG = 0x34
