class BILBO_SystemAddresses(enum.IntEnum):
    # 0x01 - 0x0F : SYSTEM / FIRMWARE
    FIRMWARE_STATE = 0x01
    FIRMWARE_TICK = 0x02
    FIRMWARE_REVISION = 0x03
    BOARD_REVISION = 0x04

    FIRMWARE_DEBUGFUNCTION = 0x05
    FIRMWARE_BEEP = 0x06

    EXTERNAL_LED = 0x07
    ALL_EXTERNAL_LEDS = 0x08
    DEBUG_1_RW = 0x09

    # 0xF0 - 0xFF : SYSTEM SPECIAL
    FIRMWARE_RESET = 0xF0


